let typeValue = null

switch (typeValue) {
    case true:
        console.log("The value is true");
        break;
    case false:
        console.log("The value is false");
        break;
    case null:
        console.log("The value is null");
        break;
    case undefined:
        console.log("The value is undefined");
        break;
    default:
        console.log("The value is of an unexpected type");
}